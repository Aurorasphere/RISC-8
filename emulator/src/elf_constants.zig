const SoC = @import("soc.zig");

pub const SECTION_HEADER_SIZE = 13;
pub const MAX_SECTION_COUNT = 8;

pub const ELF_HEADER_BASE = 0;
pub const ELF_HEADER_SIZE = 6;
pub const SECTION_HEADERS_SIZE = SECTION_HEADER_SIZE * 2; // 13 * 2 = 26;

pub const SECTION_HEADERS_BASE = ELF_HEADER_BASE + ELF_HEADER_SIZE; // 0x0000 + 6 = 0x0006
pub const SECTION_DATA_BASE = SECTION_HEADERS_BASE + SECTION_HEADERS_SIZE; // 0x0006 + 0x001A = 0x0020

pub const INST_SECTION_FILE_OFFSET = SECTION_DATA_BASE; // 0x0020
pub const DATA_SECTION_FILE_OFFSET = INST_SECTION_FILE_OFFSET + SoC.INSTR_MEM_SIZE; // 0x0020 + 0x10000 = 0x10020
pub const TOTAL_ELF_SIZE = DATA_SECTION_FILE_OFFSET + SoC.DATA_MEM_SIZE; // 0x10020 + 0x10000 = 0x20020
